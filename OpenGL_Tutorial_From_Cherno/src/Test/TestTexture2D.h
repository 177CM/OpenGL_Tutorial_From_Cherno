		void OnUpdate(float deltaTime) override;
		void OnRender() override;
		void OnImGuiRender() override;
	private:

		//use smart ptr here is because we want to allocate memory in the heap but we don't want to 
		//free them by our own, so we use unique ptr to change the heap allocated memory into a
		//scope-related memory, they will all automatically delete by program.

		std::unique_ptr<VertexArray> m_VAO;
		std::unique_ptr<VertexBuffer> m_VertexBuffer;
		std::unique_ptr<IndexBuffer> m_IndexBuffer;
		std::unique_ptr<Shader> m_Shader;
		std::unique_ptr<Texture> m_Texture;

